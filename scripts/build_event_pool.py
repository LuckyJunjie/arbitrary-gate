def parse_extended_events_py(path):
    """
    Parse add() calls from gen_extended_events.py.
    Signature: add('EVnnn','title','dynasty','location','description',weight,'era')
    """
    events = {}
    # Pattern: add('EVxxx','title','dynasty','location','desc',weight,'era')
    # description can contain commas so we match carefully
    pattern = re.compile(
        r"add\(\s*'(EV\d+)',\s*'([^']*)',\s*'([^']*)',\s*'([^']*)',\s*'([^']*)',\s*(\d+),\s*'([^']*)'\s*\)"
    )
    with open(path, encoding="utf-8") as f:
        content = f.read()
    for m in pattern.finditer(content):
        card_no = m.group(1)
        weight = int(m.group(6))
        events[card_no] = {
            "card_no": card_no,
            "title": m.group(2),
            "dynasty": m.group(3),
            "location": m.group(4),
            "description": m.group(5),
            "weight": weight,
            "rarity": weight_to_rarity(weight),
            "era": m.group(7),
            "year": None,   # will infer from era/title
        }
    return events

