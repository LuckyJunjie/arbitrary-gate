def parse_sql_file(path):
    """Extract (card_no, title, dynasty, year, location, lat, lng, description, rarity, identity_options) rows."""
    events = {}
    pattern = re.compile(
        r"\('(EV\d+)',\s*'(.*?)',\s*'(.*?)',\s*(-?\d+),\s*'(.*?)',\s*(-?\d+\.?\d*),\s*(-?\d+\.?\d*),\s*'(.*?)',\s*(\d+),\s*'(.*?)'\)",
        re.DOTALL
    )
    with open(path, encoding="utf-8") as f:
        content = f.read()
    for m in pattern.finditer(content):
        card_no = m.group(1)
        opts_raw = m.group(10)
        # Try to parse identity_options as JSON
        try:
            opts = json.loads(opts_raw)
        except Exception:
            opts = None
        events[card_no] = {
            "card_no": card_no,
            "title": m.group(2),
            "dynasty": m.group(3),
            "year": int(m.group(4)),
            "location": m.group(5),
            "latitude": float(m.group(6)),
            "longitude": float(m.group(7)),
            "description": m.group(8),
            "rarity": int(m.group(9)),
            "identity_options": opts,
            "identity_options_raw": opts_raw,
        }
    return events

