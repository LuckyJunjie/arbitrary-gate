# 4. Parse gen_events_ev269.py → EV269-EV369 (full format add() calls)
# Signature: add(ev, title, dyn, year, loc, lat, lng, desc, rarity, high, mid, low, hd, ld)
# ──────────────────────────────────────────────────────────────────────────────

def parse_gen_events_ev269(path):
    """
    Parse full-format add() calls from gen_events_ev269.py.
    Returns (card_no, row_dict) for direct SQL insertion.
    """
    events = {}
    # Match: add("EVnnn","title","dyn",year,"loc",lat,lng,"desc",rarity,"high","mid","low","hd","ld")
    pattern = re.compile(
        r'add\(\s*"(EV\d+)",\s*"([^"]*)",\s*"([^"]*)",\s*(-?\d+),\s*"([^"]*)",\s*(-?\d+\.?\d*),\s*(-?\d+\.?\d*),\s*"([^"]*)",\s*(\d+),\s*"([^"]*)",\s*"([^"]*)",\s*"([^"]*)",\s*"([^"]*)",\s*"([^"]*)"\s*\)'
    )
    with open(path, encoding="utf-8") as f:
        content = f.read()
    for m in pattern.finditer(content):
        card_no = m.group(1)
        title = m.group(2)
        dynasty = m.group(3)
        year = int(m.group(4))
        location = m.group(5)
        lat = float(m.group(6))
        lng = float(m.group(7))
        desc = m.group(8)
        rarity = int(m.group(9))
        high = m.group(10)
        mid = m.group(11)
        low = m.group(12)
        hd = m.group(13)
        ld = m.group(14) if len(m.groups()) >= 14 else None

        opts = identity_options(high, mid, low, desc_high=hd, desc_low=ld)
        events[card_no] = {
            "card_no": card_no,
            "title": title,
            "dynasty": dynasty,
            "year": year,
            "location": location,
            "latitude": lat,
            "longitude": lng,
            "description": desc,
            "rarity": rarity,
            "identity_options": json.loads(opts),
        }
    return events

