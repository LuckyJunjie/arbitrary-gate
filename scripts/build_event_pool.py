def parse_batch_json(path):
    with open(path, encoding="utf-8") as f:
        data = json.load(f)
    events = {}
    for e in data:
        card_no = e["card_no"]
        events[card_no] = {
            "card_no": card_no,
            "title": e["title"],
            "dynasty": e["dynasty"],
            "year": e["year"],
            "location": e["location"],
            "latitude": e.get("lat") or e.get("latitude"),
            "longitude": e.get("lng") or e.get("longitude"),
            "description": e["description"],
            "rarity": e["rarity"],
            "high": e["high"],
            "mid": e["mid"],
            "low": e["low"],
        }
    return events

