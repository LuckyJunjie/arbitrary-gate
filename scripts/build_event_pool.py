# 5. Build SQL row string from event dict
# ──────────────────────────────────────────────────────────────────────────────

def make_sql_row(e):
    opts = e.get("identity_options")
    if opts is None:
        # Auto-generate from high/mid/low
        opts = identity_options(e["high"], e["mid"], e["low"])
    opts_json = json.dumps(opts, ensure_ascii=False)
    return (
        f"('{e['card_no']}','{escape(e['title'])}','{escape(e['dynasty'])}',"
        f"{e['year']},'{escape(e['location'])}',{e['latitude']},{e['longitude']},"
        f"'{escape(e['description'])}',{e['rarity']},'{escape(opts_json)}')"
    )

