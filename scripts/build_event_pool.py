def weight_to_rarity(w):
    """Convert weight (100/80/50/20) to rarity (4/3/2/1)."""
    if w >= 90: return 4
    if w >= 65: return 3
    if w >= 35: return 2
    return 1

def escape(s):
    """Escape single-quotes and backslashes for SQL string literals."""
    if s is None:
        return ""
    return str(s).replace("\\", "\\\\").replace("'", "\\'").replace("\n", " ").replace("\r", " ")

def identity_options(high, mid, low, desc_high=None, desc_mid=None, desc_low=None):
    """
    Build identity_options JSON from high/mid/low identity names.
    desc_* are optional perspective strings; if None, auto-generated.
    """
    if desc_high is None:
        desc_high = f"你是{high}，在历史事件中扮演核心角色"
    if desc_mid is None:
        desc_low_fallback = f"你是{mid}，见证这一历史时刻"
        desc_mid = f"你是{mid}，在历史事件中扮演重要角色"
    if desc_low is None:
        desc_low = f"你是{low}，以普通人的视角见证这一重大历史事件"

    opts = [
        {"identity": high,  "perspective": f"你是{high}，{desc_high}"},
        {"identity": mid,   "perspective": f"你是{mid}，{desc_mid}"},
        {"identity": low,   "perspective": f"你是{low}，{desc_low}"},
    ]
    return json.dumps(opts, ensure_ascii=False)

