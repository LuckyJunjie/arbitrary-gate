def make_identity(high, mid, low):
    opts = [
        {"identity": high, "perspective": f"你是{high}，{id_perspective(high)}。"},
        {"identity": mid, "perspective": f"你是{mid}，{id_perspective(mid)}。"},
        {"identity": low, "perspective": f"你是{low}，{id_perspective(low)}。"},
    ]
    return json.dumps(opts, ensure_ascii=False)

