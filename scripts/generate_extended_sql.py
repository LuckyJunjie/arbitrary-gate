def escape(s):
    if not isinstance(s, str):
        s = str(s)
    return s.replace('\\', '\\\\').replace("'", "''").replace('\n', ' ').replace('\r', ' ')

def make_opts(high, mid, low, hd=None, ld=None):
    if hd is None:
        hd = f"你是{high}，在历史事件中扮演重要角色"
    if ld is None:
        ld = f"以普通人的视角见证这一重大历史事件"
    opts = [
        {"identity": high, "perspective": hd},
        {"identity": mid, "perspective": f"你是{mid}，在历史事件中扮演关键角色"},
        {"identity": low, "perspective": f"你是{low}，{ld}"}
    ]
    return json.dumps(opts, ensure_ascii=False)

