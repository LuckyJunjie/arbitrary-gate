# ─── parse import_event_cards.sql ────────────────────────────────────────────

def parse_import_sql():
    """Parse existing SQL file to get set of already-imported EV numbers."""
    path = os.path.join(SCRIPT_DIR, 'import_event_cards.sql')
    evs = set()
    with open(path, encoding='utf-8') as f:
        for line in f:
            m = re.search(r"'EV(\d+)'", line)
            if m:
                evs.add(int(m.group(1)))
    return evs


# ─── parse events_batch*.json ─────────────────────────────────────────────────

def parse_batch_jsons(skip_evs):
    """Parse batch JSON files, skip already-imported EV numbers."""
    entries = []
    batch_files = sorted(glob.glob(os.path.join(SCRIPT_DIR, 'events_batch*.json')))
    for bf in batch_files:
        with open(bf, encoding='utf-8') as f:
            data = json.load(f)
        for ev in data:
            num = int(ev['card_no'][2:])
            if num in skip_evs:
                continue
            # Format: card_no, title, dynasty, year, location, lat, lng, desc, rarity, identity_options
            entries.append({
                'card_no': ev['card_no'],
                'title': ev['title'],
                'dynasty': ev['dynasty'],
                'year': ev.get('year', 0),
                'location': ev.get('location', ''),
                'latitude': ev.get('latitude', 35.0),
                'longitude': ev.get('longitude', 105.0),
                'description': ev.get('description', ''),
                'rarity': ev.get('rarity', 3),
                'identity_options': ev.get('identity_options', ''),
            })
    return entries


# ─── parse gen_extended_events.py (simple format) ─────────────────────────────

def parse_extended_py(skip_evs):
    """Parse gen_extended_events.py, only return EV >= 230."""
    path = os.path.join(SCRIPT_DIR, 'gen_extended_events.py')
    with open(path, encoding='utf-8') as f:
        content = f.read()

    entries = []
    # Pattern: add('EVxxx','title','dynasty','location','description',weight,'era')
    pattern = re.compile(
        r"add\('(EV\d+)','([^']*)','([^']*)','([^']*)','([^']*)',(\d+),'([^']*)'\)"
    )
    for m in pattern.finditer(content):
        ev_num = int(m.group(1)[2:])
        if ev_num < 230:
            continue
        if ev_num in skip_evs:
            continue
        title = m.group(2)
        dynasty = m.group(3)
        location = m.group(4)
        description = m.group(5)
        weight = int(m.group(6))
        era = m.group(7)
        rarity = rarity_for(weight)
        lat, lng = coords_for(location)
        iopts = identity_options(title, dynasty, high_role=era, theme=f"{era}时期")
        year = _year_from_extended(ev_num, title, dynasty)

        entries.append({
            'card_no': m.group(1),
            'title': title,
            'dynasty': dynasty,
            'year': year,
            'location': location,
            'latitude': lat,
            'longitude': lng,
            'description': description,
            'rarity': rarity,
            'identity_options': iopts,
        })
    return entries


