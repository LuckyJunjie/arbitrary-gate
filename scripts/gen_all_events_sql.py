def esc(s):
    """Escape single quotes and backslashes for SQL."""
    if not isinstance(s, str):
        s = str(s)
    return s.replace('\\', '\\\\').replace("'", "\\'")


def rarity_for(weight):
    """Map weight/score to rarity: 5=传说, 4=稀有, 3=普通."""
    if weight >= 95:
        return 5
    elif weight >= 75:
        return 4
    else:
        return 3


